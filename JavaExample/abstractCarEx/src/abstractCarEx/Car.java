package abstractCarEx;

public abstract class Car {	
	public abstract void drive();
	public abstract void stop();
	
	public void turnOn() {
		System.out.println("시동을 켭니다.");
	}
	public void turnOff() {
		System.out.println("시동을 끕니다.");
	}
	
	public final void run() {
		turnOn();
		drive();
		stop();
		turnOff();
	}
}
